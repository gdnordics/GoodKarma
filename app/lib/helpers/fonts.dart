
import 'package:flutter/painting.dart';

class KarmaFonts {

  static TextStyle edoSZ({
    Color? color,
    Color? backgroundColor,
    double? fontSize
  }) {
    return TextStyle(
      color: color,
      fontSize: fontSize,
      fontFamily: "Edo SZ"
    );
  }

  static TextStyle paniPuri({
    Color? color,
    Color? backgroundColor,
    double? fontSize
  }) {
    return TextStyle(
      color: color,
      fontSize: fontSize,
      fontFamily: "Pani Puri"
    );
  }
}